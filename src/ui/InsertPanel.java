     private void updateDetails(){
        
        try{
           int id = 0;
           list=BookList.booklist;
            for(Book book:list){
                id=book.getBookId();
            }
            txt_bookId.setText(String.valueOf(id+1));   
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }
     
